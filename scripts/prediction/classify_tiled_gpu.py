def extract_center_pixel(lat, lon, emb_dir, patch_size=64):
    emb_path = emb_dir / f'embedding_{lat:.6f}_{lon:.6f}.npy'
    if not emb_path.exists():
        return None
    try:
        img = np.load(emb_path)
        center = patch_size // 2
        if img.ndim == 3:
            if img.shape[0] == 64:
                return img[:, center, center]
            elif img.shape[2] == 64:
                return img[center, center, :]
        return None
    except Exception:
        return None


def load_training_data(train_csv, val_csv, emb_dir):
    """Load training and validation features from embedding patches."""
    emb_dir = Path(emb_dir)

    def extract(df, label):
        features, labels = [], []
        for _, row in tqdm(df.iterrows(), total=len(df), desc=f'  {label}'):
            if pd.notna(row['lat']) and pd.notna(row['lon']):
                feat = extract_center_pixel(row['lat'], row['lon'], emb_dir)
                if feat is not None:
                    features.append(feat)
                    labels.append(int(row['has_yew']))
        return np.array(features, np.float32), np.array(labels, np.int32)

    train_df = pd.read_csv(train_csv)
    val_df = pd.read_csv(val_csv)
    print(f"  CSVs: {len(train_df)} train, {len(val_df)} val")

    X_train, y_train = extract(train_df, 'Train')
    X_val, y_val = extract(val_df, 'Val')

    # Combine train+val for final model (same as the SVM approach)
    X_all = np.vstack([X_train, X_val])
    y_all = np.concatenate([y_train, y_val])
    X_all = np.nan_to_num(X_all, nan=0.0, posinf=0.0, neginf=0.0)
    X_val = np.nan_to_num(X_val, nan=0.0, posinf=0.0, neginf=0.0)

    print(f"  Total: {len(X_all)} samples ({y_all.sum()} yew, {len(y_all)-y_all.sum()} non-yew)")
    print(f"  Val: {len(X_val)} ({y_val.sum()} yew)")

    return X_all, y_all, X_val, y_val


def load_annotation_features(annotations_csv, tile_cache, tile_info, weight=3):
    """Extract 64-band features from tiles at annotation pixel locations.
    
    Maps each annotation's (px_row, px_col) back to the correct tile file
    and extracts the embedding feature vector.
    
    Args:
        annotations_csv: Path to CSV with lat,lon,has_yew,px_row,px_col,prob
        tile_cache: Path to directory with emb_R_C.npy tile files
        tile_info: Parsed tile_info.json dict
        weight: Number of times to repeat annotation samples
    
    Returns:
        X: (N*weight, 64) feature array
        y: (N*weight,) label array
    """
    df = pd.read_csv(annotations_csv)
    # Deduplicate by px_row, px_col
    before = len(df)
    df = df.drop_duplicates(subset=['px_row', 'px_col'])
    if len(df) < before:
        print(f"  Removed {before - len(df)} duplicate annotations")
    
    print(f"  Annotations: {len(df)} unique ({(df['has_yew']==1).sum()} yew, "
          f"{(df['has_yew']==0).sum()} not-yew)")
    
    n_rows = tile_info['n_rows']
    n_cols = tile_info['n_cols']
    
    # Build tile shape lookup
    tile_shapes = {}
    for t in tile_info['tiles']:
        tile_shapes[(t['row'], t['col'])] = t['shape']  # [height, width]
    
    # Compute row heights and column widths (matching stitch_tiles logic)
    row_heights = []
    for r in range(n_rows):
        heights = [tile_shapes[(r, c)][0] for c in range(n_cols)]
        row_heights.append(min(heights))
    
    col_widths = [tile_shapes[(0, c)][1] for c in range(n_cols)]
    
    # Cumulative offsets
    row_offsets = [0]
    for h in row_heights:
        row_offsets.append(row_offsets[-1] + h)
    col_offsets = [0]
    for w in col_widths:
        col_offsets.append(col_offsets[-1] + w)
    
    # Cache loaded tiles to avoid re-reading
    tile_cache_dict = {}
    features = []
    labels = []
    skipped = 0
    
    for _, row in df.iterrows():
        pr, pc = int(row['px_row']), int(row['px_col'])
        
        # Find tile row
        tile_r = None
        for r in range(n_rows):
            if pr < row_offsets[r + 1]:
                tile_r = r
                local_r = pr - row_offsets[r]
                break
        
        # Find tile col
        tile_c = None
        for c in range(n_cols):
            if pc < col_offsets[c + 1]:
                tile_c = c
                local_c = pc - col_offsets[c]
                break
        
        if tile_r is None or tile_c is None:
            skipped += 1
            continue
        
        # Load tile if not cached
        key = (tile_r, tile_c)
        if key not in tile_cache_dict:
            tile_path = tile_cache / f"emb_{tile_r}_{tile_c}.npy"
            if tile_path.exists():
                tile_cache_dict[key] = np.load(tile_path)
            else:
                skipped += 1
                continue
        
        emb_tile = tile_cache_dict[key]
        H, W, C = emb_tile.shape
        
        if 0 <= local_r < H and 0 <= local_c < W:
            feat = emb_tile[local_r, local_c, :]
            if not np.all(feat == 0) and not np.any(np.isnan(feat)):
                features.append(feat)
                labels.append(int(row['has_yew']))
            else:
                skipped += 1
        else:
            skipped += 1
    
    # Free tile cache
    del tile_cache_dict
    
    if skipped > 0:
        print(f"  Skipped {skipped} annotations (out of bounds or zero embedding)")
    
    if len(features) == 0:
        print("  WARNING: No valid annotation features extracted!")
        return np.empty((0, 64), dtype=np.float32), np.empty(0, dtype=np.int32)
    
    X = np.array(features, dtype=np.float32)
    y = np.array(labels, dtype=np.int32)
    
    # Repeat annotations for more influence
    if weight > 1:
        X = np.tile(X, (weight, 1))
        y = np.tile(y, weight)
        print(f"  Annotation features: {len(features)} unique × {weight} = {len(y)} samples")
    else:
        print(f"  Annotation features: {len(features)} samples")
    
    return X, y


def load_gee_negatives(csv_path, weight=1, val_fraction=0.0, seed=42):
    """Load GEE-extracted non-yew negative embeddings.
    
    Args:
        csv_path: Path to CSV with emb_0..emb_63, lat, lon columns
        weight: Number of times to repeat (default: 1)
        val_fraction: Fraction to hold out for validation (0.0 = no split)
        seed: Random seed for splitting
    
    Returns:
        If val_fraction == 0:
            X: (N*weight, 64) feature array
            y: (N*weight,) label array (all zeros)
        If val_fraction > 0:
            X_train, y_train, X_val, y_val
    """
    df = pd.read_csv(csv_path)
    emb_cols = [c for c in df.columns if c.startswith('emb_')]
    if len(emb_cols) != 64:
        raise ValueError(f"Expected 64 embedding columns, found {len(emb_cols)}")
    
    X = df[emb_cols].values.astype(np.float32)
    X = np.nan_to_num(X, nan=0.0, posinf=0.0, neginf=0.0)
    
    # Filter out rows where all bands are zero (ocean/invalid)
    valid = np.any(X != 0, axis=1)
    X = X[valid]
    
    print(f"  GEE negatives: {valid.sum()} valid embeddings from {csv_path}")

    if val_fraction > 0:
        # Split into train and val
        rng = np.random.RandomState(seed)
        n = len(X)
        n_val = int(n * val_fraction)
        indices = rng.permutation(n)
        val_idx = indices[:n_val]
        train_idx = indices[n_val:]
        
        X_train = X[train_idx]
        X_val = X[val_idx]
        y_train = np.zeros(len(X_train), dtype=np.int32)
        y_val = np.zeros(len(X_val), dtype=np.int32)
        
        if weight > 1:
            X_train = np.tile(X_train, (weight, 1))
            y_train = np.tile(y_train, weight)
        
        print(f"  Split: {len(X_train)} train ({len(train_idx)} unique × {weight}), "
              f"{len(X_val)} val")
        return X_train, y_train, X_val, y_val
    else:
        y = np.zeros(len(X), dtype=np.int32)
        if weight > 1:
            X = np.tile(X, (weight, 1))
            y = np.tile(y, weight)
            print(f"  Total: {valid.sum()} valid × {weight} = {len(y)} samples")
        else:
            print(f"  Total: {valid.sum()} samples")
        return X, y


