@torch.no_grad()
def classify_tile_gpu(embedding, model, scaler, device, batch_size=500000):
    """Classify a tile on GPU. Returns (H, W) probability grid."""
    H, W, C = embedding.shape
    flat = embedding.reshape(-1, C)
    flat = np.nan_to_num(flat, nan=0.0, posinf=0.0, neginf=0.0)
    flat_scaled = scaler.transform(flat).astype(np.float32)

    probs = np.zeros(flat.shape[0], dtype=np.float32)

    for i in range(0, len(flat_scaled), batch_size):
        end = min(i + batch_size, len(flat_scaled))
        batch = torch.from_numpy(flat_scaled[i:end]).to(device)
        logits = model(batch)
        probs[i:end] = torch.sigmoid(logits).cpu().numpy()

    return probs.reshape(H, W)


