    # 6. Statistics
    # -------------------------------------------------------------------------
    pixel_area_ha = (scale ** 2) / 10000.0
    flat = prob_grid.flatten()

    print(f"\n{'='*70}")
    print("YEW DETECTION STATISTICS")
    print(f"{'='*70}")
    print(f"Total pixels: {len(flat):,}")
    print(f"Mean probability: {flat.mean():.4f}")
    print(f"Median probability: {np.median(flat):.4f}")
    print()
    for thresh in [0.3, 0.5, 0.7, 0.9]:
        count = int((flat >= thresh).sum())
        area = count * pixel_area_ha
        pct = 100 * count / len(flat)
        print(f"  P≥{thresh}: {count:>10,} pixels ({pct:5.2f}%) = {area:>10.1f} ha")

