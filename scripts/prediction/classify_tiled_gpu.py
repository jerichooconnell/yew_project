def train_mlp(X_all, y_all, X_val, y_val, scaler, device, epochs=100, lr=0.001, batch_size=512):
    """Train MLP on all data, report metrics on validation subset."""
    # Scale
    X_all_s = scaler.transform(X_all).astype(np.float32)
    X_val_s = scaler.transform(X_val).astype(np.float32)

    # Tensors
    X_t = torch.from_numpy(X_all_s).to(device)
    y_t = torch.from_numpy(y_all.astype(np.float32)).to(device)
    X_val_t = torch.from_numpy(X_val_s).to(device)
    y_val_t = torch.from_numpy(y_val.astype(np.float32)).to(device)

    dataset = TensorDataset(X_t, y_t)
    loader = DataLoader(dataset, batch_size=batch_size, shuffle=True)

    model = YewMLP(input_dim=X_all.shape[1]).to(device)
    optimizer = torch.optim.Adam(model.parameters(), lr=lr, weight_decay=1e-4)
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=epochs)
    criterion = nn.BCEWithLogitsLoss()

    best_f1 = 0
    best_state = None
    patience_counter = 0

    for epoch in range(epochs):
        model.train()
        total_loss = 0
        for xb, yb in loader:
            optimizer.zero_grad()
            logits = model(xb)
            loss = criterion(logits, yb)
            loss.backward()
            optimizer.step()
            total_loss += loss.item() * len(xb)
        scheduler.step()

        # Validate every 5 epochs
        if (epoch + 1) % 5 == 0 or epoch == epochs - 1:
            model.eval()
            with torch.no_grad():
                val_logits = model(X_val_t)
                val_probs = torch.sigmoid(val_logits).cpu().numpy()
                val_preds = (val_probs >= 0.5).astype(int)
                val_labels = y_val

                acc = accuracy_score(val_labels, val_preds)
                f1 = f1_score(val_labels, val_preds)
                auc = roc_auc_score(val_labels, val_probs)

            avg_loss = total_loss / len(X_all)
            print(f"  Epoch {epoch+1:3d}/{epochs}: loss={avg_loss:.4f}, "
                  f"val_acc={acc:.4f}, val_F1={f1:.4f}, val_AUC={auc:.4f}")

            if f1 > best_f1:
                best_f1 = f1
                best_state = {k: v.cpu().clone() for k, v in model.state_dict().items()}
                patience_counter = 0
            else:
                patience_counter += 1
                if patience_counter >= 6:  # 30 epochs without improvement
                    print(f"  Early stopping at epoch {epoch+1}")
                    break

    # Load best model
    if best_state:
        model.load_state_dict(best_state)
    model.eval()

    # Final metrics
    with torch.no_grad():
        val_logits = model(X_val_t)
        val_probs = torch.sigmoid(val_logits).cpu().numpy()
        val_preds = (val_probs >= 0.5).astype(int)
        metrics = {
            'accuracy': float(accuracy_score(y_val, val_preds)),
            'f1_score': float(f1_score(y_val, val_preds)),
            'roc_auc': float(roc_auc_score(y_val, val_probs)),
            'n_train': len(X_all),
            'epochs': epochs,
            'best_f1': float(best_f1),
        }
    print(f"\n  Best model: acc={metrics['accuracy']:.4f}, F1={metrics['f1_score']:.4f}, "
          f"AUC={metrics['roc_auc']:.4f}")

    return model, metrics


